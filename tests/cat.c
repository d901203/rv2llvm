#define FRAME_WIDTH 64
#define FRAME_HEIGHT 64

#include <stdio.h>
#include <stdlib.h>
#include <time.h>

#define WIDTH 80
#define HEIGHT 24

const char *colors[256] = {NULL};
const char *output = "  ";

static int min_row = -1, max_row = -1;
static int min_col = -1, max_col = -1;

static void finish() {
    printf("\033[?25h\033[0m\033[H\033[2J");
    exit(0);
}

static void update_window_size() {
    min_col = (FRAME_WIDTH - WIDTH / 2) / 2;
    max_col = (FRAME_WIDTH + WIDTH / 2) / 2;

    min_row = (FRAME_HEIGHT - (HEIGHT - 1)) / 2;
    max_row = (FRAME_HEIGHT + (HEIGHT - 1)) / 2;
}

static void newline(int n) {
    for (int i = 0; i < n; ++i)
        putc('\n', stdout);
}

int main(int argc, char **argv) {
    /* Assume xterm */
    {
        colors[','] = "\033[48;5;17m";
        colors['.'] = "\033[48;5;231m";
        colors['\''] = "\033[48;5;16m";
        colors['@'] = "\033[48;5;230m";
        colors['$'] = "\033[48;5;175m";
        colors['-'] = "\033[48;5;162m";
        colors['>'] = "\033[48;5;196m";
        colors['&'] = "\033[48;5;214m";
        colors['+'] = "\033[48;5;226m";
        colors['#'] = "\033[48;5;118m";
        colors['='] = "\033[48;5;33m";
        colors[';'] = "\033[48;5;19m";
        colors['*'] = "\033[48;5;240m";
        colors['%'] = "\033[48;5;175m";
    }

    update_window_size();
    printf("\033[H\033[2J\033[?25l");

    size_t i = 0;
    unsigned int f = 0;
    char last = 0;
    while (1) {
        printf("\033[H");
        for (int y = min_row; y < max_row; ++y) {
            for (int x = min_col; x < max_col; ++x) {
                char color;
                if (y > 23 && y < 43 && x < 0) {
                    int mod_x = ((-x + 2) % 16) / 8;
                    if ((i / 2) % 2)
                        mod_x = 1 - mod_x;
                    const char *rainbow = ",,>>&&&+++###==;;;,,";
                    color = rainbow[mod_x + y - 23];
                    if (color == 0)
                        color = ',';
                } else if (x < 0 || y < 0 || y >= FRAME_HEIGHT ||
                           x >= FRAME_WIDTH) {
                    color = ',';
                } else {
                    color = frames[i][y][x];
                }

                if (color != last && colors[(int)color]) {
                    last = color;
                    printf("%s%s", colors[(int)color], output);
                } else {
                    printf("%s", output);
                }
            }
            newline(1);
        }

        last = 0;
        if (++f == 60)
            finish();
        if (!frames[++i])
            i = 0;

        struct timespec ts = {.tv_sec = 0, .tv_nsec = 90000};
        nanosleep(&ts, &ts);
    }

    return 0;
}
